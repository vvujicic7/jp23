public class Zadatak1 {
	public static void main(String[] args) {
		
	
	
	// Program ispisuje sve brojeve od 20 200
	
	// program ispisuje sve parne brojeve od 1 do 57
	
	// program ispisuje od 100 do 1
	
	// program ispisuje sve neparne brojeve od 100 do 21
	
	for (int i=20;i<200;i++) {
		System.out.println(i);
		}
	for (int i=1;i<57;i+=2) {
		System.out.printf("%d ", i);
		}
	for (int i=100;i>1;i--) {
		System.out.printf("%d ",i);
		}	
	for (int i=99;i>21;i-=2) {
		System.out.println(i);
		}
	}
}